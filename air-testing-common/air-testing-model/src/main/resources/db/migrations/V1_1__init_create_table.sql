CREATE TABLE `device_info` (
  `ID` bigint(20) NOT NULL AUTO_INCREMENT COMMENT '设备ID，表主键',
  `DEVICE_CODE` varchar(40) NOT NULL COMMENT '设备标识码',
  `DEVICE_DESC` varchar(100) NOT NULL COMMENT '设备描述',
  `ADDRESS` varchar(200) DEFAULT NULL COMMENT '设备地址',
  `LONGITUDE` float(9,6) DEFAULT NULL COMMENT '经度 以度为单位的经度值，精确到百万分之一度',
  `LATITUDE` float(8,6) DEFAULT NULL COMMENT '纬度 以度为单位的纬度值，精确到百万分之一度',
  `SIM` varchar(20) DEFAULT NULL COMMENT 'SIM卡号',
  `PROTOCOL` varchar(40) NOT NULL COMMENT '协议类别',
  `COLLECT_INTERVAL` int(6) DEFAULT '10' COMMENT '设备采集信息的间隔',
  `CREATETIME` datetime NOT NULL COMMENT '创建时间',
  `UPDATETIME` datetime DEFAULT NULL COMMENT '更新时间',
  PRIMARY KEY (`ID`),
  UNIQUE KEY `DEVICE_CODE_IDX` (`DEVICE_CODE`) USING BTREE,
  KEY `DEVICE_CREATETIME_IDX` (`CREATETIME`) USING BTREE,
  KEY `DEVICE_DESC_IDX` (`DEVICE_DESC`) USING BTREE
